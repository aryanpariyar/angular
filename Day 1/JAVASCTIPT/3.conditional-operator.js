// Conditional Operators

// ( == != > < >= <=)
// A comparision operator compares its operands and returns a logical value based
// On whether the comparision is true.

var a = 10;
var b = 20;

console.log(a == b);
console.log(a != b);
console.log(a > b);
console.log(a < b);
console.log(a >= b);
console.log(a <= b);